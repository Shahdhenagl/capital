  function t(key) {
    return i18n[currentLang][key] || i18n.ar[key] || key;
  }

  function localField(item, arKey, enKey) {
    return currentLang === "en" ? (item[enKey] || item[arKey]) : item[arKey];
  }

  function normalizePhone(phone) {
    const digits = String(phone || "").replace(/\D/g, "");
    if (digits.startsWith("00")) return digits.slice(2);
    if (digits.startsWith("0")) return "2" + digits;
    return digits || "201120442206";
  }

  function applyLanguage() {
    document.documentElement.lang = currentLang;
    document.documentElement.dir = currentLang === "ar" ? "rtl" : "ltr";
    document.body.dir = currentLang === "ar" ? "rtl" : "ltr";
    document.body.dataset.lang = currentLang;
    document.querySelectorAll("[data-i18n]").forEach(el => {
      el.textContent = t(el.dataset.i18n);
    });
    document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
      el.placeholder = t(el.dataset.i18nPlaceholder);
    });
    const toggle = document.getElementById("langToggle");
    if (toggle) toggle.textContent = currentLang === "ar" ? "EN" : "AR";
  }

